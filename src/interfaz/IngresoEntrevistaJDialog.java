    public boolean chequeo () {
        boolean flag = true;
        if (jTextAreaComent.getText().isBlank()) {
            flag = false;
        } else {
            if ((int) jSpinnerPuntaje.getValue() > 100 || (int) jSpinnerPuntaje.getValue() < 0) {
                flag = false;
            } else {
                if (jListEval.isSelectionEmpty()) {
                    flag = false;
                } else {
                    if (jListPost.isSelectionEmpty()) {
                        flag = false;
                    }
                }
            }
        }
        return flag;
    }

